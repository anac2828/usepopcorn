const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const KEY = "4c217adc";

// MAIN APP *********

export default function App() {
  const [query, setQuery] = useState("");
  const [movies, setMovies] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [watched, setWatched] = useState([]);
  const [selectedId, setSelectedId] = useState(null);

  // const tempQuery = "interstellar";

  function handleSelectMovie(id) {
    // close movie if the same movie is clicked on
    setSelectedId((selectedId) => (id === selectedId ? null : id));
  }

  function handleCloseMovie() {
    setSelectedId(null);
  }

  function handleAddWatchedMovie(movie) {
    // a new array of objects will be created with the current watched list and the new watched movie
    setWatched((watched) => [...watched, movie]);
  }

  function handleDeleteWatchedMovie(id) {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  }

  // this will only run when the component renders for the first time when the sencond argument is an empty array
  useEffect(() => {
    // For clean up function
    const controller = new AbortController();

    const fetchMovies = async () => {
      try {
        // isLoading will display the text "Loading ..."
        setIsLoading(true);
        setError("");

        // API REQUEST
        const res = await fetch(
          `http://www.omdbapi.com/?apikey=${KEY}&s=${query}`,
          { signal: controller.signal }
        );

        // Error handler
        if (!res.ok)
          throw new Error("Something went wrong with fetching movies");

        // Convert data to json
        const data = await res.json();

        // Error handler
        if (data.Response === "False") throw new Error("Movie not found");

        // State update with return API data
        setMovies(data.Search);
        setError("");
      } catch (err) {
        if (err.name !== "AbortError") {
          setError(err.message);
        }
      } finally {
        //Removes the "Loading ..." message
        setIsLoading(false);
      }
    };

    // END OFF API CALL

    // Aborts Use effect if search query is less than 3 characters
    if (query.length < 3) {
      setMovies([]);
      setError("");
      return;
    }

    handleCloseMovie();
    // CALL API REQUEST function
    fetchMovies();

    // clean up function when query is being typed by user to prevent multiple data requests
    return function () {
      controller.abort();
    };
  }, [query]);

  return (
    <>
      {/* using children in the NavBar component elimates the use of prop drilling and we can pass the movie object directly to the component that needs it. */}
      <NavBar>
        <Logo />
        <Search query={query} setQuery={setQuery} />
        <NumResults movies={movies} />
      </NavBar>
      <Main>
        {/* element is an explicit prop. It is being used insted of the children prop */}
        {/* <Box element={<MovieList movies={movies} />} />

        <Box
          element={
            <>
              <WatchedSummary watched={watched} />
              <WatchedMovieList watched={watched} />
            </>
          }
        /> */}
        <Box>
          {/* {isLoading ? <Loader /> : <MovieList movies={movies} />} */}
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectMovie} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
              onAddWatchedMovie={handleAddWatchedMovie}
              watched={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMovieList
                watched={watched}
                onDeleteWatchedMovie={handleDeleteWatchedMovie}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

